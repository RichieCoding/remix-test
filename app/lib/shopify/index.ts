export async function getCollectionProducts({
  collection,
  reverse,
  sortKey,
}: {
  collection: string;
  reverse?: boolean;
  sortKey?: string;
}): Promise<Product[]> {
  const res = await shopifyFetch<ShopifyCollectionProductsOperation>({
    query: getCollectionProductsQuery,
    tags: [TAGS.collections, TAGS.products],
    variables: {
      handle: collection,
      reverse,
      sortKey,
    },
  });

  if (!res.body.data.collection) {
    return [];
  }

  return reshapeProducts(
    removeEdgesAndNodes(res.body.data.collection.products)
  );
}

export async function getProducts({
  query,
  reverse,
  sortKey,
}: {
  query?: string;
  reverse?: boolean;
  sortKey?: string;
}): Promise<Product[]> {
  const res = shopifyFetch<ShopifyProductsOperation>({
    query: getProductsQuery,
    tags: [TAGS.products],
    variables: {
      query,
      reverse,
      sortKey,
    },
  });

  return reshapeProducts(removeEdgesAndNodes((await res).body.data.products));
}

export async function createCustomer(
  input: CustomerInput
): Promise<CustomerCreate> {
  const res = shopifyFetch<ShopifyCreateCustomerOperation>({
    endpoint: adminEndpoint,
    headers: {
      "X-Shopify-Access-Token": adminKey,
    },
    query: createCustomerMutation,
    variables: {
      input,
    },
  });
  return (await res).body.data.customerCreate;
}
